function translate(vietnamese, hanzi) {
  // 1. Exact hanzi match — most authoritative
  if (HANZI_EN[hanzi] !== undefined) return HANZI_EN[hanzi];

  // 2. Exact Vietnamese match
  const vi = vietnamese.trim();
  if (VI_EN[vi] !== undefined) return VI_EN[vi];

  // 3. First part before comma/slash/space from Vietnamese
  const firstVi = vi.split(/[，,、/]/)[0].trim();
  if (VI_EN[firstVi] !== undefined) return VI_EN[firstVi];

  // 4. First word of Vietnamese
  const firstWord = vi.split(" ")[0];
  if (VI_EN[firstWord] !== undefined) return VI_EN[firstWord];

  // 5. First hanzi character (single character fallback)
  if (hanzi.length === 1 && HANZI_EN[hanzi[0]] !== undefined) return HANZI_EN[hanzi[0]];

  return vietnamese; // fallback — leave Vietnamese as-is
}

const content = fs.readFileSync(DATA_FILE, "utf8");
const lines = content.split("\n");
let filled = 0;
let skipped = 0;

const updated = lines.map(line => {
  // Fixed regex: english is captured in group 4, vietnamese in group 5
  const m = line.match(/\{ id: '(\d+)', hanzi: "([^"]+)", pinyin: "([^"]+)", english: "([^"]*)", vietnamese: "([^"]+)",/);
  if (!m) return line;

  const english = translate(m[5], m[2]);

  if (!english || english === m[5]) {
    skipped++;
  } else {
    filled++;
    const escaped = english.replace(/"/g, '\\"');
    return line.replace(`english: "${m[4]}"`, `english: "${escaped}"`);
  }

  return line;
});

fs.writeFileSync(DATA_FILE, updated.join("\n"), "utf8");
